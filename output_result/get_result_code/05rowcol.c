char mapPoint[row][col][2];
int closeCount=0;
int countResult=0;
int checklist[row][col];
void check(int x,int y){
    closeCount++;
    checklist[y][x]=1;
    if(checklist[y][x+1]==0&&x<(col-1)){
        check(x+1,y);
    }
    if(checklist[y+1][x]==0&&y<(row-1)){
        check(x,y+1);
    }
    if(checklist[y][x-1]==0&&x>0){
        check(x-1,y);
    }
    if(checklist[y-1][x]==0&&y>0){
        check(x,y-1);
    }
};
int checkClose(){
    for(int a=0;a<row;a++){
        for(int b=0;b<col;b++){
            checklist[a][b] = 1;
        }
    }//reset checklist
    for(int a=0;a<row;a++){
        for(int b=0;b<col;b++){
            if(dayMap[a][b]=='0'){
                checklist[a][b]=0;
            }
        }
    }//put 0 in dayMap to checkList
    /*for(int a=0;a<6;a++){
        for(int b=0;b<13;b++){
            printf("%d, ",checklist[a][b]);
        }
        printf("\n");
    }//print checklist*/
    int count1=0;
    for(int a=0;a<row;a++){
        for(int b=0;b<col;b++){
            if(checklist[a][b]==0){
                closeCount=0;
                check(b,a);
                if(closeCount%5==2 || closeCount%5==3 || closeCount%5==4){
                    return 0;
                }
                if(closeCount==1){
                    count1++;
                    if(count1>1){
                        return 0;
                    }
                }
            }
        }
    }// go check
    return 1;
}
void resetDay(){
    for(int a=0;a<row;a++){
        for(int b=0;b<col;b++){
            dayMap[a][b] = '0';
        }
    }
    for(int a=9;a<13;a++){
        dayMap[4][a] = 'a';
    }

    for(int a=0;a<row;a++){
        for(int b=0;b<col;b++){
            for(int c=0;c<2;c++){
                mapPoint[a][b][c] = '0';
            }
            
        }
    }
    for(int a=9;a<13;a++){
        mapPoint[4][a][0] = 'a';
    }
};
